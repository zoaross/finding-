type Ctx = {
  appLanguage: string;
  setAppLanguage: (code: string) => void;
  // Backward-compatible aliases while MVP routes migrate to explicit names.
  lang: string;
  setLang: (code: string) => void;
  t: (key: string, params?: Record<string, string | number>) => string;
};

const I18nCtx = createContext<Ctx | null>(null);

export function sanitizeLanguageCode(code: string | null | undefined): MvpLanguageCode {
  return isMvpLanguageCode(code) ? code : "zh";
}

export function isMvpLanguageCode(code: string | null | undefined): code is MvpLanguageCode {
  return code === "zh" || code === "en" || code === "ko";
}

export function translateUiString(
  language: string,
  key: string,
  params?: Record<string, string | number>,
): string {
  const normalized = sanitizeLanguageCode(language);
  const dict = dictionaries[normalized];
  let s = dict?.[key] ?? dictionaries.en[key] ?? dictionaries.zh[key] ?? key;
  if (params) {
    for (const [k, v] of Object.entries(params)) {
      s = s.replaceAll(`{${k}}`, String(v));
    }
  }
  return s;
}

export function I18nProvider({ children }: { children: ReactNode }) {
  const [appLanguage, setAppLanguageState] = useState<string>("zh");

  // Load on mount (avoids SSR mismatch)
  useEffect(() => {
    try {
      const v = localStorage.getItem(UI_LANG_KEY);
      const normalized = sanitizeLanguageCode(v);
      setAppLanguageState(normalized);
      if (v !== normalized) localStorage.setItem(UI_LANG_KEY, normalized);
    } catch {
      // localStorage can be unavailable in embedded browser sandboxes.
    }
    // Listen for cross-tab and same-tab changes
    const onStorage = (e: StorageEvent) => {
      if (e.key === UI_LANG_KEY && e.newValue)
        setAppLanguageState(sanitizeLanguageCode(e.newValue));
    };
    const onCustom = (e: Event) => {
      const code = (e as CustomEvent<string>).detail;
      if (code) setAppLanguageState(sanitizeLanguageCode(code));
    };
    window.addEventListener("storage", onStorage);
    window.addEventListener(UI_LANG_CHANGED_EVENT, onCustom as EventListener);
    return () => {
      window.removeEventListener("storage", onStorage);
      window.removeEventListener(UI_LANG_CHANGED_EVENT, onCustom as EventListener);
    };
  }, []);

  const setAppLanguage = useCallback((code: string) => {
    const normalized = sanitizeLanguageCode(code);
    setAppLanguageState(normalized);
    try {
      localStorage.setItem(UI_LANG_KEY, normalized);
      window.dispatchEvent(new CustomEvent(UI_LANG_CHANGED_EVENT, { detail: normalized }));
    } catch {
      // Ignore storage failures; in-memory language still updates.
    }
  }, []);

  const t = useCallback(
    (key: string, params?: Record<string, string | number>): string => {
      return translateUiString(appLanguage, key, params);
    },
    [appLanguage],
  );

  useEffect(() => {
    if (typeof document === "undefined") return;
    const source = dictionaries.zh;
    const target = dictionaries[appLanguage] ?? dictionaries.en;
    const hardcoded = hardcodedTextTranslations[appLanguage] ?? {};
    const exact = new Map<string, string>();
    for (const key of Object.keys(source)) {
      const zhText = source[key];
      const targetText = target[key] ?? dictionaries.en[key];
      if (zhText && targetText) exact.set(zhText, targetText);
    }
    for (const [zhText, targetText] of Object.entries(hardcoded)) {
      exact.set(zhText, targetText);
    }
    const partials = [...exact.entries()]
      .filter(([from, to]) => from && to && from !== to)
      .sort((a, b) => b[0].length - a[0].length);
    const translateTextValue = (input: string) => {
      const trimmed = input.trim();
      const exactHit = exact.get(trimmed);
      if (exactHit) return input.replace(trimmed, exactHit);
      if (appLanguage === "zh") return input;
      let output = input;
      for (const [from, to] of partials) {
        if (output.includes(from)) output = output.replaceAll(from, to);
      }
      return output;
    };
    const translateElementAttrs = (element: HTMLElement) => {
      if (element.closest("[data-no-i18n='true']")) return;
      const originalAttrKey = (attr: string) =>
        `i18nOriginal${attr
          .split("-")
          .map((part) => part.charAt(0).toUpperCase() + part.slice(1))
          .join("")}`;
      for (const attr of ["placeholder", "aria-label", "title"]) {
        const value = element.getAttribute(attr);
        if (!value) continue;
        const datasetKey = originalAttrKey(attr);
        if (appLanguage === "zh") {
          const original = element.dataset[datasetKey];
          if (original) element.setAttribute(attr, original);
          continue;
        }
        const translated = translateTextValue(value);
        if (!translated || translated === value) continue;
        element.dataset[datasetKey] = value;
        element.setAttribute(attr, translated);
      }
    };
    const translateNode = (node: Node) => {
      if (node.nodeType !== Node.TEXT_NODE) return;
      const parent = node.parentElement;
      if (!parent || ["SCRIPT", "STYLE", "TEXTAREA", "INPUT"].includes(parent.tagName)) return;
      if (parent.closest("[data-no-i18n='true']")) return;
      const value = node.textContent ?? "";
      const trimmed = value.trim();
      if (!trimmed) return;
      if (appLanguage === "zh") {
        const original = parent.dataset.i18nOriginalText;
        if (original) node.textContent = value.replace(trimmed, original);
        return;
      }
      const translated = translateTextValue(value);
      if (!translated || translated === value) return;
      parent.dataset.i18nOriginalText = trimmed;
      node.textContent = translated;
    };
    const walk = (root: ParentNode) => {
      const walker = document.createTreeWalker(root, NodeFilter.SHOW_TEXT);
      let node = walker.nextNode();
      while (node) {
        translateNode(node);
        node = walker.nextNode();
      }
      if (root instanceof HTMLElement) translateElementAttrs(root);
      root.querySelectorAll?.("[placeholder],[aria-label],[title]").forEach((element) => {
        if (element instanceof HTMLElement) translateElementAttrs(element);
      });
    };
    walk(document.body);
    const observer = new MutationObserver((mutations) => {
      for (const mutation of mutations) {
        mutation.addedNodes.forEach((node) => {
          if (node.nodeType === Node.TEXT_NODE) translateNode(node);
          else if (node instanceof HTMLElement) walk(node);
        });
      }
    });
    observer.observe(document.body, { childList: true, subtree: true });
    return () => observer.disconnect();
  }, [appLanguage]);

  const contextValue = useMemo(
    () => ({
      appLanguage,
      setAppLanguage,
      lang: appLanguage,
      setLang: setAppLanguage,
      t,
    }),
    [appLanguage, setAppLanguage, t],
  );

  return <I18nCtx.Provider value={contextValue}>{children}</I18nCtx.Provider>;
}

export function useI18n(): Ctx {
  const ctx = useContext(I18nCtx);
  if (!ctx) {
    // Safe fallback when used outside provider (e.g. SSR before hydration)
    return {
      appLanguage: "zh",
      setAppLanguage: () => {},
      lang: "zh",
      setLang: () => {},
      t: (k: string, params?: Record<string, string | number>) => {
        let s = dictionaries.zh[k] ?? k;
        if (params)
          for (const [pk, pv] of Object.entries(params)) s = s.replaceAll(`{${pk}}`, String(pv));
        return s;
      },
    };
  }
  return ctx;
}

/**
 * Reactive hook for the AI-translation preferred language.
 * Reads localStorage + listens for changes (storage event from other tabs,
 * or a same-tab CustomEvent dispatched whenever the value changes).
 */
export function usePrefLang(
  storageKey: string,
  defaultValue = "zh",
): [string, (code: string) => void] {
  const [value, setValue] = useState<string>(sanitizeLanguageCode(defaultValue));

  useEffect(() => {
    try {
      const v = localStorage.getItem(storageKey);
      const normalized = sanitizeLanguageCode(v);
      setValue(normalized);
      if (v !== normalized) localStorage.setItem(storageKey, normalized);
    } catch {
      // localStorage can be unavailable in embedded browser sandboxes.
    }
    const onStorage = (e: StorageEvent) => {
      if (e.key === storageKey && e.newValue) setValue(sanitizeLanguageCode(e.newValue));
    };
    const onCustom = (e: Event) => {
      const detail = (e as CustomEvent<{ key: string; code: string }>).detail;
      if (detail && detail.key === storageKey) setValue(sanitizeLanguageCode(detail.code));
    };
    window.addEventListener("storage", onStorage);
    window.addEventListener(PREF_LANG_CHANGED_EVENT, onCustom as EventListener);
    return () => {
      window.removeEventListener("storage", onStorage);
      window.removeEventListener(PREF_LANG_CHANGED_EVENT, onCustom as EventListener);
    };
  }, [storageKey]);

  const set = useCallback(
    (code: string) => {
      const normalized = sanitizeLanguageCode(code);
      setValue(normalized);
      try {
        localStorage.setItem(storageKey, normalized);
        window.dispatchEvent(
          new CustomEvent(PREF_LANG_CHANGED_EVENT, {
            detail: { key: storageKey, code: normalized },
          }),
        );
      } catch {
        // Ignore storage failures; in-memory preference still updates.
      }
    },
    [storageKey],
  );

  return [value, set];
}

